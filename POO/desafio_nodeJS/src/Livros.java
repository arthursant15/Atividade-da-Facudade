public class Livros {
    int id;
    String titulo;
    String autor;
    int anoDePublicacao;
    String genero;

    public Livros(int id,String titulo, String autor, String genero, int anoDePublicacao){ //Construtor
        this.id = id;
        this.titulo = titulo;
        this.autor = autor;
        this.genero = genero;
        this.anoDePublicacao = anoDePublicacao;
    }
}



